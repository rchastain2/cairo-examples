void draw_curve(cairo_t* ctx, double* c, int len)
{
  cairo_move_to(ctx, c[0], c[1]);
  int i = 2;

  while (i < len)
  {
    cairo_rel_curve_to(ctx, c[i], c[i + 1], c[i + 2], c[i + 3], c[i + 4], c[i + 5]);
    i += 6;
  }

  cairo_close_path(ctx);
}

